#include <bits/stdc++.h>
using namespace std;

#define min(a, b) (((a) < (b)) ? (a) : (b))
#define PI 3.14

int main()
{

    int a = 18;
    int b = 76;

    cout << "Minimum value between " << a << " and " << b << " is: " << min(a, b) << endl;
    ///////////////////////////////////////////////////////////////////////////
    double area;
    area = PI * a * a;

    cout << "area is : " << area << endl;

    return 0;
}

// Minimum value between 18 and 76 is: 18
// area is : 1017.36
